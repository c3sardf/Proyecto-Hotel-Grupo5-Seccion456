    public void listar() {
        modeloTabla.setRowCount(0); 
        
        for (int i = 0; i < controlador.tamano(); i++) {
            modelo.Empleado e = controlador.obtener(i);
            Object[] fila = {
                e.getDni(),
                e.getNombre(),
                e.getApellido(),
                e.obtenerRol()
            };
            modeloTabla.addRow(fila);
        }
    }
    void limpiar() {
        txtDni.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtPassword.setText("");
        txtDni.setEditable(true); 
        txtDni.requestFocus();
    }

    private boolean esNumerico(String texto) {
        if (texto == null || texto.isEmpty()) {
            return false;
        }

        for (int i = 0; i < texto.length(); i++) {
            char c = texto.charAt(i);

            if (!Character.isDigit(c)) { 
                return false; 
            }
        }
        return true;
    }
