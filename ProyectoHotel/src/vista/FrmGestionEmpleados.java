    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    // Obtenemos los datos eliminando espacios en blanco (trim)
        String dni = txtDni.getText().trim();
        String nom = txtNombre.getText().trim();
        String ape = txtApellido.getText().trim();
        String pass = txtPassword.getText().trim();
        

        if (dni.isEmpty() || nom.isEmpty() || ape.isEmpty() || pass.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "ERROR: Todos los campos son obligatorios.");
            return; 
        }

        
        if (!esNumerico(dni)) {
            javax.swing.JOptionPane.showMessageDialog(this, "ERROR: El DNI debe contener solo números.");
            return;
        }
        
    
        if (dni.length() != 8) {
            javax.swing.JOptionPane.showMessageDialog(this, "ERROR: El DNI debe tener exactamente 8 dígitos.");
            return;
        }


        if (controlador.buscar(dni) != null) {
            javax.swing.JOptionPane.showMessageDialog(this, "ERROR: El DNI " + dni + " ya existe en el sistema.");
            return;
        }

        //si pasa todas las validaciones guardamos:
        try {
            String rol = cboRol.getSelectedItem().toString();
            modelo.Empleado e;
            
            //aplicamos Polimorfismo según el combo
            if (rol.equals("Administrador")) {
                e = new modelo.Administrador(dni, nom, ape, pass);
            } else {
                e = new modelo.Recepcionista(dni, nom, ape, pass);
            }

            controlador.adicionar(e);
            
            listar();  //actualizartabla
            limpiar(); //borrarcajas
            
            javax.swing.JOptionPane.showMessageDialog(this, "Empleado registrado correctamente.");
            
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error inesperado: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpleadosMouseClicked

    int fila = tblEmpleados.getSelectedRow();

    if (fila != -1) { 

        modelo.Empleado e = controlador.obtener(fila);
        

        txtDni.setText(e.getDni());
        txtNombre.setText(e.getNombre());
        txtApellido.setText(e.getApellido());
        txtPassword.setText(e.getPassword());
        cboRol.setSelectedItem(e.obtenerRol());
        

        txtDni.setEditable(false); 
    }
    }//GEN-LAST:event_tblEmpleadosMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      String dni = txtDni.getText();

    //preguntar antes de borrar 
    int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar al empleado con DNI: " + dni + "?");

    if (respuesta == 0) { 
        controlador.eliminar(dni);
        listar(); 
        limpiar(); 
        javax.swing.JOptionPane.showMessageDialog(this, "Eliminado correctamente");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    try {
        String dni = txtDni.getText();
        

        modelo.Empleado e = controlador.buscar(dni);
        
        if (e != null) {

            e.setNombre(txtNombre.getText());
            e.setApellido(txtApellido.getText());
            e.setPassword(txtPassword.getText());
            

            
            listar();
            limpiar();
            javax.swing.JOptionPane.showMessageDialog(this, "Datos actualizados");
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "El DNI no existe");
        }
        
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnModificarActionPerformed

