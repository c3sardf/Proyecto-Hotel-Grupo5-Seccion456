    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String numStr = txtNumero.getText().trim(); 
        String capStr = txtCapacidad.getText().trim();
        String preStr = txtPrecio.getText().trim();
        String tipo = cboTipo.getSelectedItem().toString();


        if (numStr.isEmpty() || capStr.isEmpty() || preStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }
        

        if (!esNumerico(numStr)) {
             JOptionPane.showMessageDialog(this, "El Número de Habitación debe ser un entero.");
             return;
        }
        if (!esNumerico(capStr) || !esNumerico(preStr)) {
            JOptionPane.showMessageDialog(this, "Capacidad y Precio deben ser números.");
            return;
        }


        int num = Integer.parseInt(numStr);

        if (controlador.buscar(num) != null) {
            JOptionPane.showMessageDialog(this, "El número de habitación " + num + " ya existe.");
            return;
        }


        int cap = Integer.parseInt(capStr);
        double pre = Double.parseDouble(preStr);
        
        Habitacion h = new Habitacion(num, tipo, cap, pre);
        controlador.adicionar(h);
        
        listar();
        limpiar();
        JOptionPane.showMessageDialog(this, "Habitación registrada.");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
     String numStr = txtNumero.getText().trim();
        
        if (!esNumerico(numStr)) {
             JOptionPane.showMessageDialog(this, "El numero no es válido.");
             return;
        }
        
        int num = Integer.parseInt(numStr); 
        Habitacion h = controlador.buscar(num); 
        
        if (h != null) {
            try {
                h.setTipo(cboTipo.getSelectedItem().toString());
                h.setCapacidad(Integer.parseInt(txtCapacidad.getText()));
                h.setPrecio(Double.parseDouble(txtPrecio.getText()));
                
                listar();
                limpiar();
                JOptionPane.showMessageDialog(this, "Habitación actualizada.");
            } catch(Exception ex) {
                JOptionPane.showMessageDialog(this, "Error en los datos numéricos.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error: No se encuentra la habitación.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
     String numStr = txtNumero.getText();
        
        if (esNumerico(numStr)) {
             int num = Integer.parseInt(numStr);
             
             if (controlador.buscar(num) != null) {
                 int r = JOptionPane.showConfirmDialog(this, "¿Eliminar habitación " + num + "?");
                 if (r == 0) {
                     controlador.eliminar(num); 
                     listar();
                     limpiar();
                 }
             }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

