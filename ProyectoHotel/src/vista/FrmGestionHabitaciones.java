    private void configurarTabla() {
        modeloTabla.addColumn("NUMERO");
        modeloTabla.addColumn("TIPO");
        modeloTabla.addColumn("CAPACIDAD");
        modeloTabla.addColumn("PRECIO");
        modeloTabla.addColumn("ESTADO");
        tblHabitaciones.setModel(modeloTabla);
    }


public void listar() {

        modeloTabla.setRowCount(0);
        

        for (int i = 0; i < controlador.tamano(); i++) {
            Habitacion h = controlador.obtener(i);
            Object[] fila = {
                h.getNumero(),
                h.getTipo(),
                h.getCapacidad(),
                h.getPrecio(),
                h.getEstado()
            };
            modeloTabla.addRow(fila);
        }
        

        actualizarStock(); 
    }


    private void limpiar() {
        txtNumero.setText("");
        txtCapacidad.setText("");
        txtPrecio.setText("");
        cboTipo.setSelectedIndex(0);
        
        txtNumero.setEditable(true); 
        txtNumero.requestFocus();
        btnGuardar.setEnabled(true);
        tblHabitaciones.clearSelection();
    }
    

    private boolean esNumerico(String texto) {
        try {
            Double.parseDouble(texto);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    private void actualizarStock() {

        int cSimple = controlador.contarPorTipo("Simple");
        int cDoble = controlador.contarPorTipo("Doble");
        int cMatri = controlador.contarPorTipo("Matrimonial");
        int cSuite = controlador.contarPorTipo("Suite");
        

        lblStockSimple.setText("Simples: " + cSimple);
        lblStockDoble.setText("Dobles: " + cDoble);
        lblStockMatrimonial.setText("Matrimoniales: " + cMatri);
        lblStockSuite.setText("Suites: " + cSuite);
    }
