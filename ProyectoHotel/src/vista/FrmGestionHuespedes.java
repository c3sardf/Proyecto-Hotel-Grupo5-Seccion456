    private void configurarTabla() {
        modeloTabla.addColumn("DNI");
        modeloTabla.addColumn("NOMBRE");
        modeloTabla.addColumn("APELLIDO");
        modeloTabla.addColumn("TELÉFONO");
        tblHuespedes.setModel(modeloTabla);
    }


    public void listar() {
        modeloTabla.setRowCount(0);
        for (int i = 0; i < controlador.tamano(); i++) {
            Huesped h = controlador.obtener(i);
            Object[] fila = {
                h.getDni(),
                h.getNombre(),
                h.getApellido(),
                h.getTelefono()
            };
            modeloTabla.addRow(fila);
        }
    }


    private void limpiar() {
        txtDni.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtTelefono.setText("");
        
        txtDni.setEditable(true); // Desbloquear DNI
        txtDni.requestFocus();
        btnGuardar.setEnabled(true);
        tblHuespedes.clearSelection();
    }


    private boolean esNumerico(String texto) {
        if (texto == null || texto.isEmpty()) return false;
        for (char c : texto.toCharArray()) {
            if (!Character.isDigit(c)) return false;
        }
        return true;
    }
