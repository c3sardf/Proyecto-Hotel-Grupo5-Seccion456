    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       String dni = txtDni.getText().trim();
        String nom = txtNombre.getText().trim();
        String ape = txtApellido.getText().trim();
        String tel = txtTelefono.getText().trim();


        if (dni.isEmpty() || nom.isEmpty() || ape.isEmpty() || tel.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        if (!esNumerico(dni)) {
            JOptionPane.showMessageDialog(this, "El DNI debe ser solo números.");
            return;
        }
        if (dni.length() != 8) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos.");
            return;
        }
        if (!esNumerico(tel)) {
            JOptionPane.showMessageDialog(this, "El teléfono debe ser numérico.");
            return;
        }
        

        if (controlador.buscar(dni) != null) {
            JOptionPane.showMessageDialog(this, "El DNI ya existe en el directorio.");
            return;
        }


        controlador.adicionar(new Huesped(dni, nom, ape, tel));
        listar();
        limpiar();
        JOptionPane.showMessageDialog(this, "Huésped registrado correctamente.");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String dni = txtDni.getText().trim();
        

        Huesped h = controlador.buscar(dni);
        
        if (h != null) {
            h.setNombre(txtNombre.getText().trim());
            h.setApellido(txtApellido.getText().trim());
            h.setTelefono(txtTelefono.getText().trim());
            
            listar();
            limpiar();
            JOptionPane.showMessageDialog(this, "Datos actualizados.");
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un huésped de la tabla.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
     String dni = txtDni.getText();
        
        if (controlador.buscar(dni) != null) {
            int r = JOptionPane.showConfirmDialog(this, "¿Eliminar al huésped " + dni + "?");
            
            if (r == 0) { // Si dijo SÍ
                controlador.eliminar(dni);
                listar();
                limpiar();
                JOptionPane.showMessageDialog(this, "Eliminado.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

