    private void btnGenerarIngresosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarIngresosActionPerformed
     try {
        DateTimeFormatter fmt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate fInicio = LocalDate.parse(txtDesde.getText(), fmt);
        LocalDate fFin = LocalDate.parse(txtHasta.getText(), fmt);
        
        if (fFin.isBefore(fInicio)) {
            JOptionPane.showMessageDialog(this, "La fecha final debe ser mayor a la inicial.");
            return;
        }

        double[] resultados = FrmGenerarReserva.controladorReserva.calcularIngresos(fInicio, fFin);
        
        double totalHosp = resultados[0];
        double totalServ = resultados[1];
        double totalGral = totalHosp + totalServ;
        
        txtIngresoHospedaje.setText("S/. " + String.format("%.2f", totalHosp));
        txtIngresoServicios.setText("S/. " + String.format("%.2f", totalServ));
        txtIngresoTotal.setText("S/. " + String.format("%.2f", totalGral));
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Fechas incorrectas. Use formato dd/mm/aaaa");
    }
    }//GEN-LAST:event_btnGenerarIngresosActionPerformed

