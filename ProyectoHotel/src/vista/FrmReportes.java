private void actualizarOcupacion() {
        int cDisp = 0;
        int cOcup = 0;
        int cMant = 0;
        
        ArregloHabitaciones hotel = FrmGestionHabitaciones.controlador;
        
        for(int i=0; i<hotel.tamano(); i++) {
            String estado = hotel.obtener(i).getEstado();
            
            if(estado.equals("Disponible") || estado.equals("Limpia")) {
                cDisp++;
            } else if (estado.equals("Ocupado")) {
                cOcup++;
            } else {
                cMant++; 
            }
        }
        
 
        txtDisp.setText(String.valueOf(cDisp));
        txtOcup.setText(String.valueOf(cOcup));
        txtMant.setText(String.valueOf(cMant));
        

        String barra = "";
        barra += "DISPONIBLES: ";
        for(int k=0; k<cDisp; k++) barra += "█";
        barra += " (" + cDisp + ")\n\n";
        
        barra += "OCUPADAS:    ";
        for(int k=0; k<cOcup; k++) barra += "█";
        barra += " (" + cOcup + ")\n\n";
        
        barra += "MANTENIM.:   ";
        for(int k=0; k<cMant; k++) barra += "█";
        barra += " (" + cMant + ")";
        
        txtGrafico.setText(barra);
    }
